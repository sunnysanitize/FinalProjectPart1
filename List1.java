public class List1 {
    public static void main(String[] args) {

        // Initializing array
        double[] letters = {1.111, 1.65, 53.0, 43.7, 1.3, 1.4, 1.5, 5.0, 7.3, 6.0};
        
        // Looping
        for (int x = 0; x < letters.length; x++) {

            // Print statement
            System.out.println(letters[x]);
        }
    }
}
